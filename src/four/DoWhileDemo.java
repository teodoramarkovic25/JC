package four;

public class DoWhileDemo {
    public static void main(String[] args) {

        //1. Početni korak petlje initisialization
        int count = 1;  //11 ispisaće jednom
        do{
            System.out.println("Count:" +count);
            count++; //2. Korak petlje ili increment
        }
        while(count<11); //3.uslov petlje



    }
}
